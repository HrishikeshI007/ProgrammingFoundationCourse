#include <stdio.h>

int main(void)
{
    //\n - insert a newline
    printf("Line1\nLine2\n");

    //\t - insert a tab
    printf("A\ttabbed\toutput.\n");

    //\' - insert a quote
    printf("He asked - \'How are you\?\'\n");

    //\\ - insert a backslash
    printf("Directory is: C:\\\n");

    return 0;
}